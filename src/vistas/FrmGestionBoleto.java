    public FrmGestionBoleto(java.awt.Frame parent, boolean modal, int accion, int id) {
        super(parent, modal);
        initComponents();
        this.accion = accion;
        this.id = id;
        cp.colocaPasajero(cboPasajero);
        ca.colocaAutobus(cboAutobus);
        if (accion!=HB.AGREGAR){
            b = (Boleto) h.consultaRegisro(Boleto.class, id);
            coloca(b);
        }
        String cadena = "";
        switch (accion){
            case HB.AGREGAR : cadena = "Alta ";
                              habilita(true);
                                       break;
            case HB.ACTUALIZAR: cadena = "Edita ";
                               habilita(true);
                                        break;
            case HB.ELIMINAR: cadena = "Elimina ";
                              habilita(false);
                              break;
            case HB.CONSULTAR: cadena = "Consulta ";
                               habilita(false);
                                         break;
        }
        cadena = cadena +"a un Boleto";
        lblTitulo.setText(cadena);
        setTitle("Boleto");
        setLocationRelativeTo (parent);
        setResizable(false);
        setVisible(true);
    }

    private void coloca(Boleto b){
        txtOrigen.setText(b.getOrigen());
        txtDestino.setText(b.getDestino());
        txtFechaSalida.setText(b.getFechaSalida());
        txtPrecio.setText(String.valueOf(b.getPrecio()));
        a = b.getAutobus();
        p = b.getPasajero();
        cboAutobus.setSelectedItem(a.getMarca());
        cboPasajero.setSelectedItem(p.getNombre());
    }
    
    
     private void habilita (boolean b){
        txtOrigen.setEnabled(b);
        txtDestino.setEnabled(b);
        txtFechaSalida.setEnabled(b);
        txtPrecio.setEnabled(b);
        cboAutobus.setEnabled(b);
        cboPasajero.setEnabled(b);
    }
     
     
 private void asigna(){
     b.setOrigen(txtOrigen.getText());
     b.setDestino(txtDestino.getText());
     b.setFechaSalida(txtFechaSalida.getText());
     b.setPrecio(Integer.parseInt(txtPrecio.getText()));
     b.setAutobus(ca.buscaAutobus(cboAutobus.getSelectedItem().toString()));
     b.setPasajero(cp.buscaPasajero(cboPasajero.getSelectedItem().toString()));
 }
 
 
      private void mensaje (String cadena){
        JOptionPane.showMessageDialog(this, cadena);
    }
      
     
 private boolean valida(){
    boolean bnd = false;
        if ("".equals(txtOrigen.getText())){
            mensaje ("Falta escribir el origen del boleto");
            txtOrigen.requestFocus();
        }else if ("".equals(txtDestino.getText())){
            mensaje ("Falta escribir el destino del boleto");
            txtDestino.requestFocus();
        }else if ("".equals(txtFechaSalida.getText())){
            mensaje ("Falta escribir la dehca de salida del boleto");
            txtFechaSalida.requestFocus();
        }else if ("".equals(txtPrecio.getText())){
            mensaje ("Falta escribir la dehca de salida del boleto");
            txtPrecio.requestFocus();
        }else bnd = true;
        return bnd;
 }

 
     private void procesa(){
        if (valida()){
            asigna();
            if (h.ejecutaTx(b, Boleto.class, accion)){
            mensaje ("Transacci√≥n  ejecutada correctamente");
            this.dispose();
            }
        }
    }


