    public FrmGestionAutobus(java.awt.Frame parent, boolean modal, int accion, int id) {
        super(parent, modal);
        initComponents();
        this.accion = accion;
        this.id = id;
        cd.colocaConductor(cboConductor);
        if (accion!=HB.AGREGAR){
            a = (Autobus) h.consultaRegisro(Autobus.class, id);
            coloca(a);
        }
        String cadena = "";
        switch (accion){
            case HB.AGREGAR : cadena = "Alta ";
                              habilita(true);
                                       break;
            case HB.ACTUALIZAR: cadena = "Edita ";
                               habilita(true);
                                        break;
            case HB.ELIMINAR: cadena = "Elimina ";
                              habilita(false);
                              break;
            case HB.CONSULTAR: cadena = "Consulta ";
                               habilita(false);
                                         break;
        }
        cadena = cadena +"a un Autobus";
        lblTitulo.setText(cadena);
        setTitle("Autobus");
        setLocationRelativeTo (parent);
        setResizable(false);
        setVisible(true);
    }
    
    private void coloca(Autobus a){
        txtMarca.setText(a.getMarca());
        txtModelo.setText(a.getModelo());
        txtColor.setText(a.getColor());
        c = a.getConductor();
        cboConductor.setSelectedItem(c.getNombre());
    }

     private void habilita (boolean b){
        txtMarca.setEnabled(b);
        txtModelo.setEnabled(b);
        txtColor.setEnabled(b);
        cboConductor.setEnabled(b);
    }
 
 private void asigna(){
     a.setMarca(txtMarca.getText());
     a.setModelo(txtModelo.getText());
     a.setColor(txtColor.getText());
     a.setConductor(cd.buscaConductor(cboConductor.getSelectedItem().toString()));
 }

  
      private void mensaje (String cadena){
        JOptionPane.showMessageDialog(this, cadena);
    }
      
 private boolean valida(){
    boolean bnd = false;
        if ("".equals(txtMarca.getText())){
            mensaje ("Falta escribir la marca del autobus");
            txtMarca.requestFocus();
        }else if ("".equals(txtModelo.getText())){
            mensaje ("Falta escribir el modelo del autobus");
            txtModelo.requestFocus();
        }else if ("".equals(txtColor.getText())){
            mensaje ("Falta escribir el color del autobus");
            txtColor.requestFocus();
        }else bnd = true;
        return bnd;
 }

     private void procesa(){
        if (valida()){
            asigna();
            if (h.ejecutaTx(a, Autobus.class, accion)){
            mensaje ("Transacci√≥n  ejecutada correctamente");
            this.dispose();
            }
        }
    }

