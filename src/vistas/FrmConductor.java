    private void acciones (int accion){
        if (tabla.getSelectedRows().length > 0){
           id = Integer.parseInt(tabla.getValueAt(tabla.getSelectedRow(), 0).toString());
           new FrmGestionConductor(this,true, accion, id); 
           cc.mostrarDatos(tabla);
        }else
        JOptionPane.showMessageDialog (this, "Debe de seleccionar un registro");
           
    }
    
